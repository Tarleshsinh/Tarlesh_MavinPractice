package Infy_Tryout_increment_Decrement_operators;

public class Switch_Case {

	public static void main(String[] args) {
		int choice = 1;
		
		switch(choice) {
		case 1: System.out.println("Pizza is ordered");
		break;
		case 2: System.out.println("Pasta is ordered");
		break;
		case 3: System.out.println("Nugget is ordered");
		break;
		
		default : System.out.println("Invalid choice");
		}
		
		
		

	}

}
