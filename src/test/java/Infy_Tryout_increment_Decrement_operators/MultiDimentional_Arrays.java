package Infy_Tryout_increment_Decrement_operators;

public class MultiDimentional_Arrays {

	public static void main(String[] args) {
		int marks [][] = {{58,70,67},{48,88,74},{54,94,75}};
		for (int i=0;i<3; i++) {
			for(int j=0;j<3;j++) {
				System.out.println(marks[i][j]+"");
		}
			System.out.println();
			
		}
		
		

	}
	
	
		
		
	
}
